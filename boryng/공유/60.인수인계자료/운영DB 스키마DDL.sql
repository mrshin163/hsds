RETURN NUMBER IS
workdays NUMBER;
/******************************************************************************
   NAME:       FN_GET_WORKDAYS
   PURPOSE: 주어진 기간내의 영업일수를 구한다.

   REVISIONS:
   Ver        Date        Author           Description
   ---------  ----------  ---------------  ------------------------------------
   1.0        2015-03-03   powerlwi       1. Created this function.

   NOTES:

   Automatically available Auto Replace Keywords:
      Object Name:     FN_GET_WORKDAYS
      Sysdate:         2015-03-03
      Date and Time:   2015-03-03, 오후 3:09:01, and 2015-03-03 오후 3:09:01
      Username:        powerlwi (set in TOAD Options, Procedure Editor)
      Table Name:       (set in the "New PL/SQL Object" dialog)

******************************************************************************/
BEGIN
   workdays := 0;


    WITH off_day AS
    (
        SELECT '20150301' dt, '삼일절' cmt FROM dual
    )
    SELECT COUNT(*) INTO  workdays
      FROM (SELECT TO_CHAR(sdt + LEVEL - 1, 'yyyymmdd') dt
                 , TO_CHAR(sdt + LEVEL - 1, 'd') d
              FROM (SELECT V_START_DATE sdt,  V_END_DATE edt FROM dual)
            CONNECT BY LEVEL <= edt - sdt + 1
            ) a
         , off_day b
     WHERE a.dt = b.dt(+)
       AND a.d NOT IN ('1', '7')
       AND b.dt IS NULL
   ;


   RETURN workdays;
   EXCEPTION
     WHEN NO_DATA_FOUND THEN
       NULL;
     WHEN OTHERS THEN
       -- Consider logging the error and then re-raise
