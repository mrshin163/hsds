    IF :New.JUNPYO_ST_CD <> '0' AND :New.JUNPYO_ST_CD IS NOT NULL THEN
        UPDATE EAC_APPROVAL
             SET APPROVAL_STATUS_CD = 'EAPR'  ,
                    UPD_DTTM = TO_CHAR(SYSDATE,'YYYYMMDDHH24MISS'),
                    UPD_USER_ID = 'ERP'
