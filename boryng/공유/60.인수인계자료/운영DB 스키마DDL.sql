-- 결재완료(NAPR)로 업데이트 한다.
-- ==================================
    IF :New.JUNPYO_ST_CD = '0' THEN
        UPDATE EAC_APPROVAL
             SET APPROVAL_STATUS_CD = 'NAPR'  ,
                    UPD_DTTM = TO_CHAR(SYSDATE,'YYYYMMDDHH24MISS'),
                    UPD_USER_ID = 'ERP'
       WHERE 1=1
           AND COMPANY_CD  = :New.COMPANY_CD
           AND APPROVAL_ID = :New.APPROVAL_ID;                        

    END IF;    

   
